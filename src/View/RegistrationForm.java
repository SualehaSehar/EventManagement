    private boolean validateFields(){
        if(txtname.getText().trim().isEmpty()){
            JOptionPane.showMessageDialog(RegistrationForm.this, "Enter name");
            txtname.grabFocus();
            return false;
        }
        else if(txtphn.getText().trim().isEmpty()){
            JOptionPane.showMessageDialog(RegistrationForm.this, "Enter Phone Number");
            txtphn.grabFocus();
            return false;
        }
        else if(RegistrationDao.isPhoneNumberExisted(txtphn.getText())){
            JOptionPane.showMessageDialog(RegistrationForm.this, "Phone Number already exists");
            txtphn.grabFocus();
            return false;
        }
        else if(cmbgender.getSelectedIndex()==0){
            JOptionPane.showMessageDialog(RegistrationForm.this, "Select Gender");;
            return false;
        }
        else if(txtaddress.getText().trim().isEmpty()){
            JOptionPane.showMessageDialog(RegistrationForm.this, "Enter address");
            txtaddress.grabFocus();
            return false;
        }
        else if(cmbusertype.getSelectedIndex()==0){
            JOptionPane.showMessageDialog(RegistrationForm.this, "Select User Type");
            return false;
        }
        else if(txtemail.getText().trim().isEmpty()){
            JOptionPane.showMessageDialog(RegistrationForm.this, "Enter email");
            txtemail.grabFocus();
            return false;
        }
        else if(!isEmailPatternValid()){
            JOptionPane.showMessageDialog(RegistrationForm.this, "Invalid Email Pattern");
            txtemail.grabFocus();
            return false;
        }
        else if(RegistrationDao.isEmailExisted(txtemail.getText())){
            JOptionPane.showMessageDialog(RegistrationForm.this, "Email already exists");
            txtemail.grabFocus();
            return false;
        }
        else if(txtpassword.getText().trim().isEmpty()){
            JOptionPane.showMessageDialog(RegistrationForm.this, "Enter password");
            txtpassword.grabFocus();
            return false;
        }
        else if(txtretypepassword.getText().trim().isEmpty()){
            JOptionPane.showMessageDialog(RegistrationForm.this, "Enter Retype Password");
            txtretypepassword.grabFocus();
            return false;
        }
        else if(!(txtpassword.getText()).equals(txtretypepassword.getText())){
            JOptionPane.showMessageDialog(RegistrationForm.this, "Password is not matched");
            //txtretypepassword.grabFocus();
            return false;
        }
        return true;
    }
    
    private boolean isEmailPatternValid(){
        String regex = "^(.+)@(.+)$";
        Pattern pattern = Pattern.compile(regex);
        Matcher matcher = pattern.matcher(txtemail.getText());
        return matcher.matches();
    }

