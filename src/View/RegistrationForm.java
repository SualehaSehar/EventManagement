    private void btnregisterActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnregisterActionPerformed
        // TODO add your handling code here:
        if(validateFields()){
            String id = UUID.randomUUID().toString();
            String name = txtname.getText();
            String gender = (String)cmbgender.getSelectedItem();
            String no = txtphn.getText();
            String address = txtaddress.getText();
            String type = (String)cmbusertype.getSelectedItem();
            String loginid = UUID.randomUUID().toString();
            String email = txtemail.getText();
            String password = txtpassword.getText();
            Login login = new Login(loginid,email,password);
            Person person;
            if(type.equals("Manager")){
                person = new Manager(id,name,gender,no,address,type);
            }
            else{
                person = new Customer(id,name,gender,no,address,type);
            }
            boolean flag = RegistrationDao.addPerson(person, login);
            if (flag){
                JOptionPane.showMessageDialog(RegistrationForm.this, "Registration Successfull");
            }
            else{
                JOptionPane.showMessageDialog(RegistrationForm.this, "Something went wrong");
            }
        }
        
        
    }//GEN-LAST:event_btnregisterActionPerformed

